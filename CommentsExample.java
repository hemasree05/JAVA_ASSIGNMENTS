public class CommentsExample {
    public static void main(String[] args) {
        
        // Single-line comment: Printing Hello World
        System.out.println("Hello, World!");

        /* 
           Multi-line comment:
           Below line prints a message to the console.
        */
        System.out.println("This is an example of multi-line comment example.");

        /** 
         * Documentation Comment:
         * The below line prints a documentation comment example.
         */
        System.out.println("This is an example of documentation comment example.");
    }
}